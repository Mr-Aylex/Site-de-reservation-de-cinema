<h4 class="title">Birds of Prey et la fantabuleuse histoire de Harley Quinn</h4>


<div class="resume">

  « Oiseaux de proie et Fantabuleuse » est une série de bandes dessinées bien connues qui suit les exploits d'un justicier nommé Harley Quinn. Il est sur la vie quotidienne de Harley Quinn, ses relations avec sa famille, et d'autres personnes à Gotham City.

L'histoire du spectacle est aussi le même que celui de la bande dessinée: Il commence quand Harley est licenciée de son travail pour l'utilisation de ses compétences criminelles pour commettre un crime sans être pris. Après l'événement humiliant, elle décide de changer ses manières en devenant un super-héros avec la mission pour attraper les criminels qui commettent des crimes sans être détectés.

Katie Goldmar a joué le rôle de Harley Quinn alors qu'elle était en train de filmer le film. Elle avait beaucoup de travail amusant sur le film parce qu'il est son premier rôle de super-héros. Elle a dit dans une interview qu'elle a été enthousiasmés par les nouveaux défis auxquels il faisait face sur le film.


